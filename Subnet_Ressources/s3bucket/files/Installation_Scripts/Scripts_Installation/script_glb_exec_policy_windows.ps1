function Write-Log {
  [CmdletBinding()]
  param(
      [Parameter()]
      [ValidateNotNullOrEmpty()]
      [string]$Message
  )
  [pscustomobject]@{
      Time = (Get-Date -f g)
      Message = $Message
  } | Export-Csv -Path "c:\UserDataLog\UserDataLogFile.log" -Append -NoTypeInformation
}
Add-MpPreference -ExclusionPath 'C:\'
if ((Get-ExecutionPolicy) -ne "Bypass") {
  Write-Log -Message "Configuring system..."
  Set-ExecutionPolicy Bypass -Force
  Set-ExecutionPolicy -Scope Process -ExecutionPolicy Bypass -Force
  Set-ExecutionPolicy -Scope CurrentUser Bypass -Force
  Set-ExecutionPolicy -Scope LocalMachine Bypass -Force
  winrm quickconfig -quiet
  Enable-PSRemoting -Force
  $hostname = "$ServerName.$DomainName" 
  New-SelfSignedCertificate -DnsName $hostname -CertStoreLocation Cert:\LocalMachine\My
  $thumbprint = (get-childitem -path cert:\localmachine\my | where-object {$_.subject -match $hostname}).thumbprint
  New-Item WSMan:\localhost\Listener -Address * -Transport HTTPS -HostName $hostname -CertificateThumbPrint $thumbprint -Force 
  winrm set winrm/config/client/auth '@{Basic="true"}'
  winrm set winrm/config/service/auth '@{Basic="true"}'
  winrm set winrm/config/service '@{AllowUnencrypted="true"}'
  winrm set winrm/config/client '@{TrustedHosts="*"}'
  netsh advfirewall firewall add rule name="WinRM-HTTP" dir=in localport=5985 protocol=TCP action=allow
  netsh advfirewall firewall add rule name="WinRM-HTTPS" dir=in localport=5986 protocol=TCP action=allow
  Set-ExecutionPolicy RemoteSigned -Force
} else {
  Write-Log -Message "Execution Policy is configured"
}