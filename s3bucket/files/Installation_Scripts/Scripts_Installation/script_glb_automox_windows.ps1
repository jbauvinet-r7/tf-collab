function Write-Log {
  [CmdletBinding()]
  param(
      [Parameter()]
      [ValidateNotNullOrEmpty()]
      [string]$Message
  )
  [pscustomobject]@{
      Time = (Get-Date -f g)
      Message = $Message
  } | Export-Csv -Path "c:\UserDataLog\UserDataLogFile.log" -Append -NoTypeInformation
}
Set-ExecutionPolicy Bypass -Scope Process -Force

$service = Get-Service -Name "Automox Agent" -ErrorAction SilentlyContinue
if($service -eq $null){
    New-Item -Path "C:\s3-downloads\" -Name "Automox_Agent" -ItemType "directory"
    Write-Log -Message "Created Automox_Agent folder to store Agent files."
    Write-Log -Message "Getting Automox_Agent files from S3 Bucket."
    Set-ExecutionPolicy UnRestricted -Force
    $command = "[Net.ServicePointManager]::SecurityProtocol = [Net.SecurityProtocolType]::Tls12"
    Invoke-Expression $command
    Invoke-WebRequest -Uri "https://awscli.amazonaws.com/AWSCLIV2.msi" -Outfile C:\AWSCLIV2.msi
    $arguments = "/i `"C:\AWSCLIV2.msi`" /quiet"
    Start-Process msiexec.exe -ArgumentList $arguments -Wait
    $env:Path = [System.Environment]::GetEnvironmentVariable("Path","Machine")
    aws s3 cp s3://$Bucket_Name/AutomoxAgent/Automox_Installer-1.44.8.msi C:\s3-downloads\Automox_Agent\
    msiexec /i c:\s3-downloads\Automox_Agent\Automox_Installer-1.44.8.msi /l*v c:\s3-downloads\Automox_Agent\installation.log ACCESSKEY=3060ea58-01d9-4dae-b803-cc2a26d09264 -GroupName "CloudCity" /quiet 
    Write-Log -Message "Installation Automox Agent"
} else {
    Write-Log -Message "Automox Agent already installed."
}